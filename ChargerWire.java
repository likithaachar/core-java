class ChargerWire {
    public static void main(String[] args){
        String type; // declaration
        System.out.println("I am printing declaration part of type:");
        type = "USB-C"; // initialization
        System.out.println("I am printing initialization part of type: " + type);
        String brand; // declaration
        System.out.println("I am printing declaration part of brand:");
        brand = "Samsung"; // initialization
        System.out.println("I am printing initialization part of brand: " + brand);
        double length; // declaration
        System.out.println("I am printing declaration part of length:");
        length = 1.5; // initialization (in meters)
        System.out.println("I am printing initialization part of length: " + length);
        int price; // declaration
        System.out.println("I am printing declaration part of price:");
        price = 299; // initialization
        System.out.println("I am printing initialization part of price: " + price);
        boolean isFastCharging; // declaration
        System.out.println("I am printing declaration part of isFastCharging:");
        isFastCharging = true; // initialization
        System.out.println("I am printing initialization part of isFastCharging: " + isFastCharging);
        long serialNumber; // declaration
        System.out.println("I am printing declaration part of serialNumber:");
        serialNumber = 12345678901L; // initialization
        System.out.println("I am printing initialization part of serialNumber: " + serialNumber);
    }
}