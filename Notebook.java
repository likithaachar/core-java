class Notebook {
    // Static Variables
    static String manufacturer = "Naveen";
    static String coverType = "Hard Cover";
    static int editionYear = 2024;
    public static void main(String[] args) {
        // Local Variables 
        String subject = "Mathematics";
        int pagesUsed = 120;
        double weight = 350.5;
        System.out.println("Manufacturer: " + manufacturer);
        System.out.println("Cover Type: " + coverType);
        System.out.println("Edition Year: " + editionYear);
        System.out.println("Subject: " + subject);
        System.out.println("Pages Used: " + pagesUsed);
        System.out.println("Weight (grams): " + weight);
    }
}